package org.example;

import java.time.LocalDate;
import java.util.Objects;

public class TodoItem {

    //Fields
    private int id;
    String title;
    String description;
    LocalDate deadLine;
    boolean done;
    Person creator;


    //Getters & Setters

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        if (title == null || title.trim().equals("")){
            throw new IllegalArgumentException("Not allowed");
        }
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public LocalDate getDeadLine() {
        return deadLine;
    }

    public void setDeadLine(LocalDate deadLine) {
        if (deadLine == null ){
            throw new IllegalArgumentException("Not allowed");
        }
        this.deadLine = deadLine;
    }

    public boolean isDone() {
        return done;
    }

    public void setDone(boolean done) {
        this.done = done;
    }

    public Person getCreator() {
        return creator;
    }

    public void setCreator(Person creator) {
        this.creator = creator;
    }


    public boolean isOverdue() {
        LocalDate currentDate = LocalDate.now();
        if (!done) {
            if (currentDate.isAfter(deadLine)) {
                System.out.println("It is overdue and Work is not done.");
                    return true;
                } else {
                    System.out.println("It is not overdue but the Work is not done");
                    return false;
                }
            }
        return false;
        }

    @Override
    public String toString() {
        return "TodoItem{" +
                "id=" + id +
                ", title='" + title + '\'' +
                ", description='" + description + '\'' +
                ", deadLine=" + deadLine +
                ", done=" + done +
                '}';
    }


    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        TodoItem todoItem = (TodoItem) o;
        return id == todoItem.id && done == todoItem.done && Objects.equals(title, todoItem.title) &&
                Objects.equals(description, todoItem.description) && Objects.equals(deadLine, todoItem.deadLine);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, title, description, deadLine, done);
    }
}
